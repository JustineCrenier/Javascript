function surface(){
	var a =  prompt('Quel est la longueur du rectangle en cm');
	var b = prompt('Quel est la largeur du rectangle en cm');

	var rep = a*b;
	alert(rep + 'cm');
}

function perimetre(){
	var a = prompt('Quel est la longueur du rectangle en cm');
	var b = prompt('Quel est la largeur du rectangle en cm');

	var rep = (Number(a)+Number(b))*2;
	alert(rep + 'cm');
}